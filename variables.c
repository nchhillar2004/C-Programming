#include <stdio.h>

int main(){
    int a = 10;
    float b = 8.5;
    char c = 'u';
    int d = 40 + a;
    // %d for integers
    printf("The value of a is %d \n", a);
    // %f for real values/ float values
    printf("The value of b is %f \n", b);
    // %c for characters/ string
    printf("The value of c is %c \n", c);
    printf("The value of d is %d \n", d);
    return 0;
}